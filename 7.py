from fractions import Fraction


frac1 = Fraction(3, 4)
frac2 = Fraction(5, 6)

print(f"Сложение: {frac1} + {frac2} = {frac1 + frac2}")
print(f"Вычитание: {frac1} - {frac2} = {frac1 - frac2}")
print(f"Умножение: {frac1} * {frac2} = {frac1 * frac2}")
print(f"Деление: {frac1} / {frac2} = {frac1 / frac2}")
